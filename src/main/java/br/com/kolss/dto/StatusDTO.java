package br.com.kolss.dto;

public class StatusDTO {
	
	private Long id;
	private String nome;
	
	public StatusDTO() {
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
